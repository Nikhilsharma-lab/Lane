-- §4.1.2 — accept_invite_membership (idempotent, new check order)
-- Check order (locked per spec line 240 + Pre-B1.b decision):
--   1. NOT FOUND on invite lookup    -> RAISE 'Invalid invite link'
--   2. expiry                         -> RAISE 'This invite has expired...'
--   3. email match                    -> RAISE 'This invite was sent to a
--                                              different email address'
--   4. idempotency (accepted_at)      -> early-return with sentinel if
--                                        acceptor isn't a member of the
--                                        invite's org (NULL::uuid signal)
-- Creates: profile if absent, workspace_members, audit_log entry
-- (member.joined). Updates: invites.accepted_at + invites.accepted_by.
-- Deferrals:
--   - project_members INSERT (needs invites.team_id from 0012/B2)
--   - workspace_members.role is hardcoded 'member'; mapping from
--     invites.role deferred to B2 per spec gap.
--   - audit_log event_data omits 'team_id' key; reinstate in 0012.
CREATE OR REPLACE FUNCTION public.accept_invite_membership(
  invite_token text,
  target_user_id uuid,
  target_full_name text,
  target_email text
)
RETURNS TABLE (org_id uuid, profile_created boolean)
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  invite_row public.invites%ROWTYPE;
  existing_profile public.profiles%ROWTYPE;
  created_profile boolean := false;
BEGIN
  -- 1. Invite lookup
  SELECT *
  INTO invite_row
  FROM public.invites
  WHERE token = invite_token
  LIMIT 1;

  IF NOT FOUND THEN
    RAISE EXCEPTION 'Invalid invite link';
  END IF;

  -- 2. Expiry check (moved above accepted_at check per spec line 240)
  IF now() > invite_row.expires_at THEN
    RAISE EXCEPTION 'This invite has expired. Ask your team lead to send a new one.';
  END IF;

  -- 3. Email match check
  IF lower(trim(target_email)) <> lower(trim(invite_row.email)) THEN
    RAISE EXCEPTION 'This invite was sent to a different email address';
  END IF;

  -- 4. Idempotency guard (accepted_at IS NOT NULL)
  -- If invite was already accepted, determine whether the caller is already
  -- a member of the invite's org:
  --   - Yes  -> success no-op: return the org_id (repeat click by the real
  --            acceptor, e.g. browser refresh)
  --   - No   -> return sentinel (NULL::uuid) so the caller can show a
  --            "this invite has already been used by someone else" UI
  --            without leaking which user consumed it
  IF invite_row.accepted_at IS NOT NULL THEN
    IF EXISTS (SELECT 1 FROM public.profiles
               WHERE id = target_user_id AND org_id = invite_row.org_id) THEN
      RETURN QUERY SELECT invite_row.org_id, false;
      RETURN;
    ELSE
      RETURN QUERY SELECT NULL::uuid, false;
      RETURN;
    END IF;
  END IF;

  -- Profile: create if absent (preserves existing cast invite_row.role::public.role)
  SELECT *
  INTO existing_profile
  FROM public.profiles
  WHERE id = target_user_id
  LIMIT 1;

  IF NOT FOUND THEN
    INSERT INTO public.profiles (id, org_id, full_name, email, role)
    VALUES (
      target_user_id,
      invite_row.org_id,
      target_full_name,
      target_email,
      invite_row.role::public.role
    );
    created_profile := true;
  END IF;

  -- workspace_members: hardcoded 'member' for invite-accepted joins.
  -- Intent-mapping from invites.role deferred to B2 (see roadmap parking lot
  -- 2026-04-22 "B2 workspace_members.role mapping").
  INSERT INTO public.workspace_members (workspace_id, user_id, role, invited_by, created_at)
  VALUES (
    invite_row.org_id,
    target_user_id,
    'member'::public.workspace_role,
    invite_row.invited_by,
    now()
  )
  ON CONFLICT (workspace_id, user_id) DO NOTHING;

  -- Path C: team-scoping INSERT into project_members deferred to migration
  -- 0012 (B2). invites.team_id and invites.team_role columns do not exist
  -- in 0011; the INSERT will be added when those columns land.

  -- Audit log (team_id key intentionally omitted; reinstate in 0012 per
  -- roadmap parking lot 2026-04-22 "0012 audit_log reinstate team_id")
  INSERT INTO public.audit_log (workspace_id, actor_user_id, event_type, event_data)
  VALUES (
    invite_row.org_id,
    target_user_id,
    'member.joined',
    jsonb_build_object(
      'invite_id', invite_row.id,
      'invited_by', invite_row.invited_by,
      'role', invite_row.role
    )
  );

  -- Mark invite accepted (both timestamp and acceptor-id)
  UPDATE public.invites
  SET accepted_at = now(),
      accepted_by = target_user_id
  WHERE id = invite_row.id;

  RETURN QUERY SELECT invite_row.org_id, created_profile;
END;
$$;

