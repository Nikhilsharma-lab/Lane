-- Small-scale backfill (§4.1.3 single-query path). Preserves profiles.created_at
-- and maps profiles.role to workspace_role enum. Role mapping:
--   profiles.role IN ('lead','admin') -> workspace_members.role = 'owner'
--   all other profile roles           -> workspace_members.role = 'member'
-- Multi-owner cases (two profiles in the same org both mapping to 'owner')
-- are resolved by the collapse CTE below.
INSERT INTO public.workspace_members (workspace_id, user_id, role, invited_by, created_at)
SELECT
  p.org_id,
  p.id,
  CASE
    WHEN p.role IN ('lead', 'admin') THEN 'owner'::public.workspace_role
    ELSE 'member'::public.workspace_role
  END,
  NULL,
  p.created_at
FROM public.profiles p
LEFT JOIN public.workspace_members wm ON wm.user_id = p.id AND wm.workspace_id = p.org_id
WHERE wm.user_id IS NULL
ON CONFLICT (workspace_id, user_id) DO NOTHING;--> statement-breakpoint

-- Multi-owner collapse (§4.1.3). Partitions by workspace_id, ranks by
-- created_at ASC (earliest owner wins). Every rank > 1 is demoted from
-- 'owner' to 'admin'. Post-collapse, every workspace has exactly one owner
-- (the earliest-joined profile with role IN ('lead','admin')).
WITH ranked_owners AS (
  SELECT workspace_id, user_id, created_at,
    ROW_NUMBER() OVER (PARTITION BY workspace_id ORDER BY created_at ASC) AS rn
  FROM public.workspace_members
  WHERE role = 'owner'::public.workspace_role
)
UPDATE public.workspace_members
SET role = 'admin'::public.workspace_role
FROM ranked_owners
WHERE workspace_members.workspace_id = ranked_owners.workspace_id
  AND workspace_members.user_id = ranked_owners.user_id
  AND ranked_owners.rn > 1;
